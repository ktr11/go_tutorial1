// Goのゼロ値
package main

import "fmt"

func main() {
	var b bool
	var i int
	var r rune
	var f float64
	var c complex64
	var s string

	fmt.Println("bool = \t\t", b)
	fmt.Println("int = \t\t", i)
	fmt.Println("rune = \t\t", r)
	fmt.Println("float64 = \t", f)
	fmt.Println("complex64 = \t", c)
	fmt.Println("string = \t\t", s)
}
